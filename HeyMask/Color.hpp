#ifndef Color_h
#define Color_h

#define COLOR_RESET        "\033[0m"            // 색상 초기화
#define COLOR_BLACK        "\033[30m"           // 검정색
#define COLOR_RED          "\033[31m"           // 빨간색
#define COLOR_GREEN        "\033[32m"           // 초록색
#define COLOR_YELLOW       "\033[33m"           // 노란색
#define COLOR_BLUE         "\033[34m"           // 파란색
#define COLOR_MAGENTA      "\033[35m"           // 자홍색
#define COLOR_CYAN         "\033[36m"           // 청록색
#define COLOR_WHITE        "\033[37m"           // 하얀색
#define COLOR_BOLD_BLACK   "\033[1m\033[30m"    // 굵은 검은색
#define COLOR_BOLD_RED     "\033[1m\033[31m"    // 굵은 빨간색
#define COLOR_BOLD_GREEN   "\033[1m\033[32m"    // 굵은 초록색
#define COLOR_BOLD_YELLOW  "\033[1m\033[33m"    // 굵은 노란색
#define COLOR_BOLD_BLUE    "\033[1m\033[34m"    // 굵은 파란색
#define COLOR_BOLD_MAGENTA "\033[1m\033[35m"    // 굵은 자홍색
#define COLOR_BOLD_CYAN    "\033[1m\033[36m"    // 굵은 청록색
#define COLOR_BOLD_WHITE   "\033[1m\033[37m"    // 굵은 하얀색

#endif /* Color_h */
